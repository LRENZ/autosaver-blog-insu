## Frequently Asked Questions

### Can I change my deductible anytime?
Yes! You can adjust deductibles when you renew your policy or sometimes mid-term. Most changes take effect immediately.

### Do I need the same deductible for all vehicles?
No. You can have different deductibles for each vehicle on your policy.

### What if I can''t afford my deductible after an accident?
Some options:
- Payment plans with repair shops
- Personal loan or credit card
- Reduce claim amount and pay difference
- In extreme cases, don''t repair and accept actual cash value

### Does my deductible apply if someone else is at fault?
No. If you file through the other driver''s insurance, there''s no deductible. If you file through your insurance first (then they subrogate), you may pay the deductible initially and get it back later.

### Can I have no deductible?
Some insurers offer $0 deductible options, but it''s very expensive. Better to save the premium and build an emergency fund.

