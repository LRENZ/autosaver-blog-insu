Your insurance deductible is one of the most important factors affecting your premium. Choose wisely, and you could save hundreds of dollars per year while maintaining adequate protection.

## What is a Car Insurance Deductible?

A deductible is the amount you pay out-of-pocket before your insurance coverage kicks in for a claim.

### Example Scenario
You have a $500 deductible and get in an accident causing $3,000 in damage:
- You pay: $500
- Insurance pays: $2,500

### Where Deductibles Apply
Deductibles typically apply to:
- **Collision Coverage**: Damage from accidents
- **Comprehensive Coverage**: Theft, vandalism, weather damage, animal strikes

Deductibles do NOT apply to:
- **Liability Coverage**: Damage you cause to others
- **Medical Payments**: Your medical bills
- **Uninsured Motorist**: Claims against uninsured drivers

## Common Deductible Amounts

Standard deductible options:
- $250 - Lowest available
- $500 - Most popular choice
- $1,000 - Good balance of savings and affordability
- $2,500 - Maximum savings
- $5,000+ - Available but rare

### Industry Statistics
- 43% of drivers choose $500 deductibles
- 28% choose $1,000 deductibles
- 18% choose $250 deductibles
- 11% choose $2,500+ deductibles

## How Much Can You Save?

Increasing your deductible significantly reduces your premium.

### Typical Savings by Deductible Change

**$250 → $500 Deductible**
- Average savings: 8-12% annually
- Example: $1,500/year premium → Save $120-180/year

**$500 → $1,000 Deductible**
- Average savings: 15-25% annually
- Example: $1,500/year premium → Save $225-375/year

**$500 → $2,500 Deductible**
- Average savings: 30-45% annually
- Example: $1,500/year premium → Save $450-675/year

